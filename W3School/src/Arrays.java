public class Arrays {
    public static void main(String[] args) {
        int[] myNumbers = {1,2,3,4};
        int i;
        for (i=0;i<4;i++){
            System.out.print(myNumbers[i] + " ");
        }
        String[] cars = {"Mazda","Bugatti","BMW","Mercedes"};
        int j;
        for (j=0;j<4;j++){
            System.out.print(cars[j] + " ");
        }
    }
}
